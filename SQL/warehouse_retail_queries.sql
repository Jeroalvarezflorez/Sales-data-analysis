SELECT s.YEAR, s.SUPPLIER, s.RETAIL_SALES, sup.COUNTRY, sup.CATEGORY
FROM sales s
INNER JOIN suppliers sup ON s.SUPPLIER = sup.SUPPLIER
WHERE s.RETAIL_SALES IS NOT NULL
LIMIT 10;

SELECT s.SUPPLIER, SUM(s.RETAIL_SALES) AS total_sales, sup.CATEGORY
FROM sales s
LEFT JOIN suppliers sup ON s.SUPPLIER = sup.SUPPLIER 
WHERE s.RETAIL_SALES IS NOT NULL
GROUP BY s.SUPPLIER, sup.CATEGORY
ORDER BY total_sales desc
LIMIT 10;

